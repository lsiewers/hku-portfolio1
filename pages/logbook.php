    <p>
      <strong>Wat heb ik gedaan?</strong>
      In een minimalistisch ontwerp waarbij het werk direct herkenbaar is door de mock-up en duidelijke titel. Voor nu zit er nog niet heel veel werk in, dus ieder werk in de portfolio
      wordt groots uitgelicht.
    </p>
    <p>
      <strong>Hoe heb ik dit ervaren?</strong>
