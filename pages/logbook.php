    <p>
    Een grote eye-opener bij Tools for Design Thinking; er is altijd een andere oplossing. Ik ben niet een opgever, maar er zijn genoeg momenten waar ik denk dat er geen andere oplossing meer is terwijl dat dus wel zo is. Ook de manieren van onthouden door visualisatie en associatie, out-of-the-box denken en altijd waarom? vragen hebben veel met mij gedaan. Ik heb er veel over nagedacht en ik ben er vaak mee bezig.
    </p>
